#include "libft.h"

int	ft_atoi(const char *str)
{
	int	s;
	int	v;

	v = 0;
	s = 1;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
	{
		str++;
	}
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
		{
			s *= -1;
		}
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		v = v * 10 + (*str - '0');
		str++;
	}
	return (s * v);
}
