#include "libft.h"

char	*ft_strrchr(char *c, int i)
{
	int	n;

	n = ft_strlen(c);
	while (n >= 0)
	{
		if (c[n] == (char)i)
			return (c + n);
		n--;
	}
	return (NULL);
}
