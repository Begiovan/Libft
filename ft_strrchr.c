char	*ft_strrchr(char *c, int i)
{
	int	n;

	n = ft_strlen(c);
	while(*c)
	{
		if(c[n] == i)
			return(c + n);
		if(n == 0)
			return(c);
		n--;
	}
	return(0);
}
